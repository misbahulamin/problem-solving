#include <bits/stdc++.h>

using namespace std;

void printDigit(int a)
{
    for(int i=1; i<=a; i++)
    {
        cout<<i<<" ";
    }
}

int main()
{
    int a;
    cin>>a;
    printDigit(a);
    return 0;
}